/** @type {import('next').NextConfig} */
const isProd = process.env.NODE_ENV === 'production'

// 'unsafe-eval' je nutný pro Next.js HMR v devu; v produkci ho vypínáme.
// 'unsafe-inline' zůstává — Next.js injectuje hydration skripty bez nonce.
const scriptSrc = isProd
  ? "script-src 'self' 'unsafe-inline'"
  : "script-src 'self' 'unsafe-inline' 'unsafe-eval'"

const csp = [
  "default-src 'self'",
  scriptSrc,
  "style-src 'self' 'unsafe-inline'",
  "img-src 'self' data: blob:",
  "font-src 'self'",
  "connect-src 'self' https://api.mymemory.translated.net",
  "frame-ancestors 'none'",
].join('; ')

const nextConfig = {
  images: {
    // Only local images — no remote patterns (prevents SSRF/DoS via Image Optimizer)
  },
  async headers() {
    return [
      {
        source: '/(.*)',
        headers: [
          { key: 'X-Content-Type-Options', value: 'nosniff' },
          { key: 'X-Frame-Options', value: 'DENY' },
          { key: 'Referrer-Policy', value: 'strict-origin-when-cross-origin' },
          { key: 'X-DNS-Prefetch-Control', value: 'on' },
          { key: 'Permissions-Policy', value: 'camera=(), microphone=(), geolocation=()' },
          { key: 'Content-Security-Policy', value: csp },
          { key: 'Strict-Transport-Security', value: 'max-age=63072000; includeSubDomains; preload' },
        ],
      },
    ]
  },
}

module.exports = nextConfig
